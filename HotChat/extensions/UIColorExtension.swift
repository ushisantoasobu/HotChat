import UIKit

extension UIColor {

    class func mainColor() -> UIColor {
        return UIColor(red: 255/255, green: 103/255, blue: 62/255, alpha: 1.0)
    }

    class func mainThinColor() -> UIColor {
        return UIColor(red: 255/255, green: 103/255, blue: 62/255, alpha: 0.75)
    }

}